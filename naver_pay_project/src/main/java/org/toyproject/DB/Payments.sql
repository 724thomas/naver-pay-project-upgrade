DROP TABLE PAYMENTS;

CREATE TABLE PAYMENTS(
        PAYMENT_ID INT,
        PRODUCT_NAME VARCHAR(255),
        PRODUCT_PRICE INT,
        PRODUCT_AMOUNT INT,
        PAYMENT_METHOD VARCHAR(255),
        PAYMENT_DATE DATE,
        SELLER_NAME VARCHAR(255),
        SELLER_PHONE VARCHAR(255),
        USER_SERIAL_NUMBER INT,
        COLLECTED_POINTS INT,
        URL VARCHAR(255),
        PRIMARY KEY (PAYMENT_ID)
        );

